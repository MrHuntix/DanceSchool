$(function(){
    var birdhover     = $('.s1');
        var birdaudio = birdhover.find('audio')[0];

    birdhover.hover(function(){
       birdaudio.play();
    }, function(){
       birdaudio.pause();
	   birdaudio.currentTime = 0;
	    
    });
});
